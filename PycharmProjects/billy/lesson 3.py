# membership operators
# in/not in

x = 400

cash = [400,600,700,300,450]

if x in cash:
    print("Found")
else:
    print("Not There")

if x not in cash:
    print("Not There")
else:
    print("Found")

# identity operators
# is/is not

a = 67
b = 89

if a is b:
    print("They are same")
else:
    print("They are not  same")

if a is not b:
    print("They are not same")
else:
    print("They are same")

